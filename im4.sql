use im;
create table if not exists FriendAskMsg
(
uid int auto_increment primary key,
askid varchar(20),
reqid varchar(20),
ask_content varchar(1000)
)default charset=utf8;

create table if not exists FriendRlyMsg
(
uid int auto_increment primary key,
sendid varchar(20),
recvid varchar(20),
reply int
)default charset=utf8;

